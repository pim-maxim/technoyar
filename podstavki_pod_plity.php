        <strong>Подставки под плиты</strong> применяются в печах, где происходит обжиг керамических изделий. Они несут на себе как тепловую нагрузку так и механическую.</br></br>
        Очень часто подставки под плиты изготавливаются из <a href="keramicheskie_roliki.php"> муллитокремнеземистой трубки (керамических роликов)</a> диаметром 34 - 52 мм.</br></br>
        <strong>Физико-химические показатели муллитокремнеземистой трубки:</strong></br>
        - содержание АlО<sub>3</sub> - не менее 52 %;</br>
        - содержание Рb<sub>2</sub>О<sub>3</sub> - не более 0,7 %;</br>
        - огнеупорность - не менее 1750 <sup>o</sup>С;</br>
        - водопоглощение - не более 0,2 %;</br>
        - термическая стойкость - число теплосмен не менее 3;</br>
        - рекомендуемая температура эксплуатации - 1350 <sup>o</sup>С.</br></br>
        Так же, очень часто, подставки под плиты делают из <strong>тяжелого огнеупорного шамотного кирпича марок ША и ШБ</strong>.</br></br>
        Шамотный кирпич является наиболее распространенным и востребованным видом огнеупорного кирпича. Он наиболее устойчивым к высоким температурам и химическим веществам одновременно. Он стал таким благодаря особенному составу:</br>
        - шамотная глина;</br>
        - порошок кокса;</br>
        - порошок графита;</br>
        - зерна кварца коричневого цвета.</br></br>
        В ходе производства шамотный кирпич подвергается обжигу при различных температурах, которые стартуют от +200 <sup>o</sup>С и постепенно вырастают до +1500 <sup>o</sup>С. Благодаря таким мерам материал легко выдерживает прямой контакт с пламенем на протяжении нескольких часов. За счет особенности состава материал имеет характерный желтый оттенок.</br></br>
        В зависимости от пропорции глины и остальных компонентов эксплуатационные характеристики кирпича могут существенно меняться. Большую роль играет также способ обработки, а именно – время запекания кирпича. Например, если материал передержать в печи, на поверхности появится характерная стекловидная пленка, которая придаст кирпичу повышенную прочность, однако одновременно его огнеупорность значительно снизится. Если, наоборот, недодержать кирпич в печи, он будет обладать повышенной гигроскопичностью и будет впитывать много влаги. Одновременно такой материал не будет обладать достаточной прочностью на изгиб и сжатие. Поэтому важно придерживаться «золотой середины».</br></br>
        Кроме устойчивости к высоким температурам (до 1730 <sup>o</sup>С), шамот также обладает стойкостью к воздействию агрессивных химических веществ:</br>
        - кислоты;</br>
        - щелочи;</br>
        - органические растворители;</br>
        - производственный или доменной шлак.</br></br>
        Именно это выделяет шамотный кирпич на фоне других видов огнеупора (углеродистый, основный и кварцевый). Благодаря таким уникальных свойствам шамотный кирпич используется для обкладки внутренних стенок мартеновских и других печей, используемых в:</br>
        - металлургии;</br>
        - химической промышленности;</br>
        - теплоэнергетике;</br>
        - обжиге керамики и фарфора,</br>
        - стекольном производстве;</br>
        - различных химических комбинатах.

        <strong>Препарат жидкого золота</strong> — это органометаллический комплекс с содержанием благородных металлов от 4 до 12%, который применяется для художественного оформления фарфорово-фаянсовых изделий и стекла – отводки, живописных работ, штампа.</br></br>
        От концентрации золота зависит оттенок золотого рисунка. Препарат имеет темно-коричневый цвет, а после обжига образует блестящую металлическую поверхность. Слой препарата толщиной приблизительно 0,1 микрона создает великолепный глянцевый эффект золота, который невозможно, повторить с помощью декоративных ненатуральных красок.Кроме того, золотой слой, получаемый с помощью препарата жидкого золота, не нуждается в дальнейшей обработке.</br></br>
        <strong>Способ применения:</strong></br>
        Поверхность, на которой в результате засияет золотая роспись, необходимо предварительно подготовить, а именно — очистить и обезжирить растворителем.
        За этим следует тонкая художественная работа — на предмет с помощью кисти тонким слоем наносится препарат жидкого золота.</br>
        Необходимо следить, чтобы наносимый слой выходил тонким и равномерным. Таким образом создается рисунок, задуманный мастером.</br>
        Для того, чтобы получить однородную золотую поверхность необходимы высокое мастерство и опыт.</br>
        Изделие с нанесенным препаратом следует высушить при комнатной температуре в течение 40-60 минут. После этого можно приступать к обжигу.</br>
        В течение 45 минут предмет обжигают при температуре 550-840 <sup>o</sup>С. Температуру обжига устанавливают в зависимости от материала обжигаемой поверхности.</br></br>
        <strong>Необходимо учесть:</strong></br>
        Золото на фарфоровых изделиях обжигается при температуре 790-810<sup>o</sup>С с выдержкой в печи 20 минут для того, чтобы золотой слой получился прочным. Золото необходимо обжигать отдельно от красок, потому как на золотой слой негативно влияют пары органических связующих. Обжиг фаянса, расписанного с помощью золота, производится при температуре 760-790 <sup>o</sup>С. На стеклянной посуде золото обжигается при температуре 540-600 <sup>o</sup>С, а на хрустале — при температуре 500-550 <sup>o</sup>С.</br></br>
        С помощью препарата жидкого золота рождаются золотые рисунки на керамических и стеклянных предметах. Поверхность, декорированная таким образом, имеет блестящий цвет настоящего золота.</br></br>
        Благодаря уникальным свойствам препарата эстетика золотого декора сохраняется десятки, а иногда и сотни лет. Примером тому служат известные художественные изделия из фарфора ХVIII-XIX вв., которые сохранились до наших дней — Кузнецовский фарфор, Батенинский фарфор, Мейссейнский фарфор, Севрский фарфор. Изделия этих мануфактур декорировались золотом. Благодаря качеству фарфора иблагородной золотой росписи этот фарфор воплотил в себе неповторимую эстетику стиля Рококо.</br></br>
        Сегодня искусство золотой росписи возрождается, внося новую веху в культуру изысканной керамики.

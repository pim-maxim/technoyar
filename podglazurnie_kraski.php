        <h2 class="uk-text-center">Подглазурные краски</h2>
        <hr class="uk-divider-small uk-text-center">
        <img src="img/ceramic/pk_logo.jpg" class="uk-border-rounded uk-animation-fade" alt="Пигменты керамические">    
        <strong>Подглазурные краски</strong> — это смесь пигментов с глазурью. Пигменты в состав подглазурных красок вводят в виде смеси оксидов металлов с добавлением каолина, оксида алюминия и др., придающих краскам устойчивость. В качестве добавок, улучшающих соответствие ТКЛР краски и ТКЛР поверхности черепка или поверхности глазури, в краску вводят глину, фарфоровую массу, ценной шпат и другие компоненты, повышающие вязкость массы, оказывающие химическое воздействие на красители при высокой температуре.</br></br>
		Для изготовления подглазурных красок в качестве пигментов применяют следующие красящие оксиды металлов: кобальта Со<sub>2</sub>О<sub>3</sub> — для получения синего и голубого цветов; никеля NiO — коричневого и фиолетового; железа Fe<sub>2</sub>O<sub>3</sub> — красного, коричневого, желтого; меди CuO зеленого, сине-зеленого; марганца Мп<sub>2</sub>О<sub>3</sub> — коричневого, фиолетового, розового; хрома Сг<sub>2</sub>О<sub>3</sub> — зеленого, красного; вольфрама WO<sub>3</sub> и урана UO<sub>3</sub> — желтого.</br></br>
		Оксиды металлов, входящие в состав подглазурных красок, должны обладать огнестойкостью, т. е. сохранять красящие свойства при температуре розлива глазури, не разлагаться, не растворяться, не вызывать дефектов глазури.</br></br>
		Подглазурные краски закрепляют на изделии обжигом. В процессе обжига цвет краски изменяется под действием температуры и газовой среды в печи. Поэтому при декорировании подглазурными красками к режиму обжига предъявляют особо строгие требования.</br></br>
        Подглазурные краски наносят на обожженные или хорошо высушенные изделия, затем покрывают слоем глазури и обжигают. Подглазурные краски рассчитаны на проявление цвета под глазурью. Цвет их зависит от толщины глазури, ее состава, условий обжига. Благодаря блестящему и прозрачному слою глазури, покрывающей подглазурные краски сверху тонким слоем, эти краски более устойчивы, чем надглазурные.</br></br>
        В зависимости от температуры обжига Подглазурные краски можно подразделить на две группы: краски с температурой обжига до 1300°С и краски с температурой обжига 1300...1450°С.</br></br>
        Подглазурные краски с температурой обжига до 1300°С применяют для декорирования керамических материалов, например фаянса, температура политого1 обжига которых не превышает 1300°С. Поэтому эта группа подглазурных красок носит название фаянсовых подглазурных пигментов и красок. Они отличаются большим разнообразием тонов и оттенков.</br></br>
        К подглазурным краскам с температурой обжига 1300...1450°С относятся Подглазурные краски для фарфора. По сравнению с фаянсовыми они имеют более бедную цветoвую палитру, так как многие из них не выдерживают воздействия высокой температуры и восстановительной среды обжига фарфоровых изделий.</br></br>
        Краски-растворы составляют особую группу подглазурных красок. От порошковых красок они отличаются тем, что их приготовляют из растворимых в воде солей. Для закрепления красок-растворов на черепке их переводят в нерастворимые в воде вещества — в безводные оксиды соответствующих металлов. Для этого декорированное изделие прокаливают при температуре 800°С. При этом цвет красок-растворов меняется. Затем изделие глазуруют и направляют на высокотемпературный обжиг. В процессе высокотемпературного обжига оксиды металлов, полученные при разложении солей во время прокаливания, взаимодействуют с составными частями черепка изделия и глазури и окрашивают их.</br></br>
        За основной тон краски берут насыщенный раствор соли в воде или смеси воды с глицерином при температуре воздуха помещения, в котором декорируют изделия.</br></br>
        Растворы для более слабых, более светлых тонов красок готовят путем добавления воды той же температуры, что и разбавленный раствор. Количество насыщенного раствора и воды определяют шкалой-палитрой и ее составом, разработанным опытным путем. Чтобы увеличить рабочую вязкость краски, в раствор добавляют сахар или декстрин.</br></br>
        К подглазурным краскам относятся также краски «высокого огня». Их используют для декорирования фарфора по обожженной глазури. Краски «высокого огня» представляют собой тонкомолотый фриттованный состав, в который входят в качестве пигментов красящие оксиды металлов, а также фарфоровая глазурь, полевой шпат или небольшое количество свинцовой фаянсовой глазури. Перед нанесением на изделие эти краски смешивают с терпентиновым маслом и скипидаром. Нанесение производят пером или кистью. Закрепляют эти краски на материале вжиганием (прокаливанием) при температуре обжига глазури в печах политого обжига. Таким образом, изделие, декорируемое красками «высокого огня», подвергается второму высокотемпературному обжигу. В результате обжига краски утопают в глазури, растворяясь в ней частично или полностью, и создают из-под глазури мягкие тона.
